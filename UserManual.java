class UserManual{
	private String manualTitle;
	private int manualId;
	private String productName;
	private String language;
	private long helplineNumber;
	private String version;
	private String publisher;
	
	
	public void setManualTitle(String manualTitle){
		this.manualTitle=manualTitle;
	}
	public String getManualTitle(){
		return this.manualTitle;
	}
	
	public void setManualId(int manualId){
		this.manualId=manualId;
	}
	public int getManualId(){
		return this.manualId;
	}
	
	public void setProductName(String productName){
		this.productName=productName;
	}
	public String getProductName(){
		return this.productName;
	}
	
	public void setLanguage(String language){
		this.language=language;
	}
	public String getLanguage(){
		return this.language;
	}
	
	public void setHelplineNumber(long helplineNumber){
		this.helplineNumber=helplineNumber;
	}
	public long getHelplineNumber(){
		return this.helplineNumber;
	}
	
	public void setVersion(String version){
		this.version=version;
	}
	public String getVersion(){
		return this.version;
	}
	
	public void setPublisher(String publisher){
		this.publisher=publisher;
	}
	public String getPublisher(){
		return this.publisher;
	}
	
	
}